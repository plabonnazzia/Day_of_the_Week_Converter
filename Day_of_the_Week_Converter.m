dayNumber = input('Enter a number (1-7): ');
switch dayNumber
    case 1
        disp('Sunday');
    case 2
        disp('Monday');
    case 3
        disp('Tuesday');
    case 4
        disp('Wednesday');
    case 5
        disp('Thursday');
    case 6
        disp('Friday');
    case 7
        disp('Saturday');
    otherwise
        disp('Invalid number. Enter a value between 1 and 7.');
end